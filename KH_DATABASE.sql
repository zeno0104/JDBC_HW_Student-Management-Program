------------------------------------------------------
CREATE TABLE KH_MAJOR (
    MAJOR_CODE  VARCHAR2(10) PRIMARY KEY,
    MAJOR_NAME  NVARCHAR2(30) UNIQUE NOT NULL,
    OFFICE      NVARCHAR2(30),
    PHONE       VARCHAR2(15)
);
